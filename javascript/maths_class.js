// Math class in JS

// PI
 console.log(Math.PI);

// abs()
//absolute value, only returns the value ignoring negative sign
console.log(Math.abs(-20));

// round(), ceil(), floor() 
const dec_num = 5.67;
console.log(Math.round(dec_num)); // 6
console.log(Math.ceil(dec_num)); // 6
console.log(Math.floor(dec_num)); // 5


// max()
// gives us the maximum of all the numbers
console.log(Math.max(1, 2, 3, 4, 67)) // 67

// min()
// gives us the minimum of all the numbers
console.log(Math.min(1, 2, 3, 4, 67)) // 1

// pow(): 2 ^ 10
console.log(Math.pow(2, 10)) // 1024

// random()
console.log(Math.random());