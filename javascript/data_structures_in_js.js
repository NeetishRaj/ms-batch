/**
 * 2 types of data types
 * 
 * 1. Primitive types: Number, String, Boolean, Null, Undefined, BigInt
 * 
 *  
 * 
 * 
 * 
 * 2. Reference types: Array[], Object{}, Function
 * 
 *  ALl reference types are an Object type in JavaScript
 */


// Number
let num = 2;
console.log(typeof num == "number");

// string
let str = "abcd";
console.log(typeof str == "string");

// boolean
let bool = true;
console.log(typeof bool == "boolean");


// undefined, it's default value for all unassigned variables
let a;
console.log(typeof a == "undefined");

// null, it's used to represent null values by ourselves
let b = null;
console.log(typeof b == "object");


